<?php
$a = (5+6) * 3 / 3 - 1;	# :D -> 10
$a += (7+3);
assert($a == 20);

$a -= (7+3);
assert($a == 10);

$a %= 2;
assert($a == 0);
$a++; $a %= 2;
assert($a == 1);

$a *= (81+ 2);
assert($a == (81+2));

$a /= (81+2);
assert($a == 1);

$a = 1;	 // 0001
$a <<= 4;  // 1000
assert($a == 16);
$a >>= 1;
assert($a == 8);

$a = 'a';
$b = 'ca_c';
assert($a . $b == 'aca_c');

$a = 3; // 11
$a &= 2; // 10
assert($a == 2); // 10

$a = 2; // 10
$a |= 1; // 01
assert($a == 3); // 11

$a = 5; //  1001
$a ^= 3; // 0011
assert($a == 6); // 1010